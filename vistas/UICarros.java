    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        DataCarros objart = new DataCarros();
        objart.setPlaca(this.placaField.getText());
        objart.setConsumo(this.consumoField.getText());
        objart.setPuestos(this.puestosField.getText());
        objart.setColor(this.colorField.getText());
        objart.setMarca(this.marcaField.getText());
        objart.setReferencia(this.referenciaField.getText());
        objart.setValor(this.costoField.getText());
        JOptionPane.showMessageDialog(null,"Se agrego " + objart.GrabarCarro() + " carro.");
        ListarArticulos();
        LimpiarCajasTexto();
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        DataCarros objart = new DataCarros();
        JOptionPane.showMessageDialog(null, "Se modifico " + objart.EditarCosto(this.costoField.getText(), this.placaField.getText()) + " clientes.");
        ListarArticulos();
        LimpiarCajasTexto();
    }//GEN-LAST:event_btnModificarActionPerformed

