    public void ListarArticulos() {
        DefaultTableModel tabla = new DefaultTableModel();
        DataCarros objart = new DataCarros();
        ArrayList<DataCarros> lista2 = new ArrayList();
        lista2 = objart.ListaCarros();
        tabla.addColumn("Placa");
        tabla.addColumn("Consumo");
        tabla.addColumn("Puestos");
        tabla.addColumn("Color");
        tabla.addColumn("Marca");
        tabla.addColumn("Referencia");
        tabla.addColumn("Costo");
        tabla.setRowCount(lista2.size());
        int i = 0;
        for (DataCarros x : lista2) {
            tabla.setValueAt(x.getPlaca(), i, 0);
            tabla.setValueAt(x.getConsumo(), i, 1);
            tabla.setValueAt(x.getPuestos(), i, 2);
            tabla.setValueAt(x.getColor(), i, 3);
            tabla.setValueAt(x.getMarca(), i, 4);
            tabla.setValueAt(x.getReferencia(), i, 5);
            tabla.setValueAt(x.getValor(), i, 6);
            i++;
        }
        this.jTable1.setModel(tabla);
    }

